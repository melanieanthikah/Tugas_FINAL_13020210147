    private void tabel_pelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_pelangganMouseClicked
        // TODO add your handling code here:
        int bar = tabel_pelanggan.getSelectedRow();
        String a = tabel_pelanggan.getValueAt(bar, 0).toString();
        String b = tabel_pelanggan.getValueAt(bar, 1).toString();
        String c = tabel_pelanggan.getValueAt(bar, 2).toString();
        String d = tabel_pelanggan.getValueAt(bar, 3).toString();
        String e = tabel_pelanggan.getValueAt(bar, 4).toString();
        String f = tabel_pelanggan.getValueAt(bar, 5).toString();
        
        txt_id.setText(a);
        txt_nama.setText(b);
        
        if("Laki-laki".equals(c)){
            btn_laki.setSelected(true);
        }else{
            btn_perempuan.setSelected(true);
        }
        selectjabatan.setSelectedItem(d);
        txt_notelp.setText(e);
        txt_alamat.setText(f);
        
    }//GEN-LAST:event_tabel_pelangganMouseClicked

